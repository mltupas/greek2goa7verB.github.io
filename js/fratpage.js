var profiles = JSON.parse(localStorage.getItem("profiles"));
var current_user = localStorage.getItem("current_user");
var user_events = [];

for (var user in profiles) {
  if (current_user == profiles[user].email) {
    user_events = profiles[user].events;
  }
}

function addToCarousel(eventToAdd) {
  var alreadyAdded = false;

  for (var index in user_events) {
    if (eventToAdd.title == user_events[index].title) {
      alreadyAdded = true;
    }
  }

  if (alreadyAdded) {
    alert("This event has already been added to your home page.");
  } else {
    user_events.push(eventToAdd);
    for (var user in profiles) {
      if (current_user == profiles[user].email) {
        profiles[user].events = user_events;
      }
    }
    alert("Event added to home page!");
  }

  localStorage.setItem("profiles", JSON.stringify(profiles));
}
