myStorage = window.localStorage;

//PULL NUMBER OF EVENTS AND ARRAY OF EVENT OBJECTS FROM LOCAL STORAGE
var num_events = myStorage.getItem("num_events") || 0;
var carousel_events = JSON.parse(myStorage.getItem("carousel_events")) || [];

function addToCarousel(eventToAdd) {
  var isInArray = false;
  console.log(carousel_events);

//IF EVENT LIST IS EMPTY, ADD EVENT OBJECT TO ARRAY LIST AND STORE IN LOCALSTORAGE
  if (carousel_events == null) {
    carousel_events.push(eventToAdd);
    num_events++;
    myStorage.setItem("carousel_events", JSON.stringify(carousel_events));
    myStorage.setItem("num_events", num_events);
  }
//CHECK EVENT LIST TO SEE IF EVENT HAS ALREADY BEEN ADDED
  else {
    for (index = 0; index < num_events; index++) {
      if (eventToAdd.title == carousel_events[index].title) {
        isInArray = true;
      }
    }
  }
  console.log(eventToAdd);
  if (isInArray == true) {
    alert('This event has already been added.');
  }
//ADD EVENT TO EVENT LIST AND STORE INTO LOCALSTORAGE
  else {
    carousel_events.push(eventToAdd);
    num_events++;
    myStorage.setItem("carousel_events", JSON.stringify(carousel_events));
    myStorage.setItem("num_events", num_events);
    alert('This event has been added to your home page!');
  }
  console.log(num_events);
  console.log(carousel_events);
}
